package com.example.demo.model;

public class Customer {
	private int customer_Id;
	private String customer_Name;
	private String customer_Address;
	private int customer_Phone;
	private String customer_loginId;
	private int customer_Password;
	public int getCustomer_Id() {
		return customer_Id;
	}
	public void setCustomer_Id(int customer_Id) {
		this.customer_Id = customer_Id;
	}
	public String getCustomer_Name() {
		return customer_Name;
	}
	public void setCustomer_Name(String customer_Name) {
		this.customer_Name = customer_Name;
	}
	public String getCustomer_Address() {
		return customer_Address;
	}
	public void setCustomer_Address(String customer_Address) {
		this.customer_Address = customer_Address;
	}
	public int getCustomer_Phone() {
		return customer_Phone;
	}
	public void setCustomer_Phone(int customer_Phone) {
		this.customer_Phone = customer_Phone;
	}
	public String getCustomer_loginId() {
		return customer_loginId;
	}
	public void setCustomer_loginId(String customer_loginId) {
		this.customer_loginId = customer_loginId;
	}
	public int getCustomer_Password() {
		return customer_Password;
	}
	public void setCustomer_Password(int customer_Password) {
		this.customer_Password = customer_Password;
	}
	

}
